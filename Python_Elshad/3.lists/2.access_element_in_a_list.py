

#        0    1    2
list = ['a', 'b', 'c']
#       -3    -2   -1


print(list[-1])


#Time Comp: O(1)
#Space Comp: O(1)

#Traversing elements of a List 
for element in list:
    print(element)

#Traversing elements of a List using index
for index in range(len(list)):
    print(list[index])


#Time Comp: O(N)
#Space Comp: O(1)